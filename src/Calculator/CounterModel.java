public class CounterModel {
    /**
     * Attribute
     */
    private int counter;

    /**
     * Getter / Setter
     */
    public int getCounter() {
        return counter;
    }

    /**
     * Counter Methode
     */
    public void incCounter(){
        this.counter++;
    }
}